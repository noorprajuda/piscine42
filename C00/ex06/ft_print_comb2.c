#include<unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		a;
	char	t[5];

	i = 0;
	a = 0;
	while (i < 99)
	{
		a = i + 1;
	
		t[0] = (i / 10) + '0';
		t[1] = (i % 10) + '0';
		t[2] = ' ';
		t[3] = (a / 10) + '0';
		t[4] = (a % 10) + '0';
		write(1, &t, 5);
		if (i != 98 && a != 99)
			write(1, ", ", 2);
		if (a == 99)
			i++;

	}
}

int	main()
{
	ft_print_comb2();
	return (0);
}
